    private void botonCrearEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearEquipoActionPerformed
        if(textNombre.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Completa el nombre del Equipo","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }else if(miembros.size()==0){
            JOptionPane.showMessageDialog(null, "Agrega por lo menos un miembro","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }else{
            //crea el equipo 
            Equipo equipo=equipoController.nuevoEquipo(textNombre.getText(), textEspecialidad.getText(), textDescripcion.getText(),miembros);      

            //obtiene los nombres de los miembros
            String nombreMiembros="{ ";
            for (Miembro miembro : miembros) {
                nombreMiembros+=miembro.getNombre()+", ";
            }
            nombreMiembros+="}";
            
            //agrega el equipo a la lista de equipos
            equipoController.agregarEquipo(equipo, nombreMiembros);
            this.setVisible(false);
        }
    }//GEN-LAST:event_botonCrearEquipoActionPerformed

    private void botonAgregarMiembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarMiembroActionPerformed
        if(textoNombreMiembro.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Agrega un nombre al integrante","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }else{
            //crea un miembro a partir de completar su nombre
            MiembroController miembroController=MiembroController.getInstance();
            Miembro m=miembroController.nuevoMiembro(textoNombreMiembro.getText(),textoRolMiembro.getText());

            miembros.add(m);
            listModel.add(listModel.getSize(), m);
            labelContador.setText(Integer.toString(miembros.size()));
            textoNombreMiembro.setText("");
            textoRolMiembro.setText("");
        }
    }//GEN-LAST:event_botonAgregarMiembroActionPerformed

